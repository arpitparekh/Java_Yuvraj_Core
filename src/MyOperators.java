public class MyOperators {

    public static void main(String[] args) {
        
        // operators  // are the symbols that we use in programming

        // arithmetic
        // + - / * %

        int a = 10;
        int b = 20;
        
        // %

        System.out.println(a % b);  // 10/20

        System.out.println(454545 % 10);

        System.out.println(13 % 2);

        System.out.println(1213412 / 10);

        // conditional operator

        // < > <= >= == !=   // gives us booealn value

        System.out.println(12<13);

        // logical operator
        //  &&  ||  !

        System.out.println(12 < 13 || 12 > 13);
        
        // increment decrement // ++ --

        // post decrement // pre decrment

        int h = 34;

        System.out.println(++h);
        System.out.println(h);

        // assignment operator
        //  =  +=  -= /= *= %=

        int k = 23;

        int f = 45;

        // f = f + 10;
        f %= 10;

        System.out.println(f);



    }
    
}
