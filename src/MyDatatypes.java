public class MyDatatypes {
    
    public static void main(String[] args) {
        
        //  datatypes

        // 8 primitive datatypes
        
        // Number datatype

        byte b = 45; // 1 byte  // 1 bytes = 16 bit // 101010101010
        System.out.println(b);
        short s = 1; // 2 bytes  // 32 bit
        System.out.println(s);

        int i = 9888885; // 4 byte  // 64 bit  // 10101010101010101010101010110
        System.out.println(i);

        long l = 343434343434343434L; // long
        System.out.println(l);

        float f = 34.34f; // 4 byte
        System.out.println(f);
        
        double d = 343434.34343434; // 8 byte
        System.out.println(d);

        char ch = '3';
        System.out.println(ch);

        boolean isLogin = false;
        System.out.println(isLogin);

        // stack  

        // array

        // object datatype
        // String
        // ArrayList 
        // collection framework

    }

}
